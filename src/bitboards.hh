// gcc built-in functions
// population count: number of set bits
#define popcount(bitboard) __builtin_popcountll(bitboard)
// index of least significant bit
#define lsb(bitboard) __builtin_ctzll(bitboard)

inline uint64_t pop_lsb(uint64_t& bitboard) {
    int square = lsb(bitboard);
    bitboard &= bitboard - 1;
    return square;
}

inline uint64_t square_bitboard(int square) {
    return 1ULL << square;
}

